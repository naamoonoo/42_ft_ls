#include "ft_ls.h"

void	free_dp(t_dp *dp)
{
	t_dp	*tmp;

	dp_set_to_head(&dp);
	while (dp)
	{
		tmp = dp;
		free(dp->curr);
		dp = dp->next;
		free(tmp);
	}
}
